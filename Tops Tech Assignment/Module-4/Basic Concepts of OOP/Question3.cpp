// What is the difference between OOP and POP?


/*
The main difference between Object-Oriented Programming (OOP) and Procedural-Oriented Programming (POP) 
lies in their design principles and structure.

Object-Oriented Programming (OOP):

Focus: OOP centers around "objects," which encapsulate data and behavior.

Structure: Programs are organized into classes and objects, promoting modularity and reusability.

Key Concepts: Utilizes concepts like encapsulation, inheritance, polymorphism, and abstraction.

Data Management: Data is bundled with methods that operate on it, which helps maintain a clear interface.

Examples: Languages include Java, C++, Python, and C#.


Procedural-Oriented Programming (POP):

Focus: POP focuses on procedures or routines (functions) to operate on data.

Structure: Programs are organized as a sequence of instructions, and data is typically separate from functions.

Key Concepts: Emphasizes functions, control structures (like loops and conditionals), and procedure calls.

Data Management: Data can be easily modified by any function, leading to potential issues with data integrity.

Examples: Languages include C, Fortran, and Pascal.
Summary of Differences:
Design: OOP is class-based; POP is function-based.

Data Handling: OOP encapsulates data; POP separates data and functions.

Reusability: OOP promotes reuse through inheritance; POP relies on function reuse.

Flexibility: OOP allows for easier modification and extension of programs due to its modular nature.

Each paradigm has its strengths and weaknesses, and the choice between them often depends on the 
specific requirements of a project.

*/